<h1>Exhibitions:</h1>
<p>
Exhibitions work because they are face-to-face. They provide us an opportunity to accelerate the selling cycle by matching needs with a company's ability to meet those needs. They work because adults are visual learners. Exhibiting is a like a portable company with a visual medium – the exhibit, products, the graphics, collateral materials and promotional products are visual elements. Buyers are also experiential. Trade shows are extremely effective to launch new products in the market.
</p>
<p>
The Meetings Industry consists of a broad range of organisers, suppliers and facilities engaged in the development and delivery of meetings, conferences, exhibitions and other related events which are held in order to achieve a range of professional, business, cultural or academic objectives.
</p>
<p>
The Meetings Industry is a distinct economic sector, with its own unique organisations, standards, priorities and communication vehicles. It is comprised largely of small to
medium sized organisations which are not as formally integrated as many other industries; however, it achieves a high degree of functional integration through extensive, ongoing exchanges amongst industry organisations. It has also had traditionally ongoing relationships with the tourism and hospitality sectors, which it supports by generating incremental demand for travel, accommodation and destination services and with which it often interacts in the processes of service delivery and destination promotion.
</p>
<p>
Specifically, the Meetings Industry is a key component of the knowledge economy, acting as a vehicle for business, professional and academic communities to achieve the interactions required for the knowledge transfer, collaboration and information dissemination which is the primary purpose of these events. The industry recognises that to be sustainable it must achieve a greater level of recognition for the benefits it delivers in relation to global economic and professional development, specifically:
</p>
<ul>
<li>Providing stimulus to global economic growth by creating forums for new product development, exchange and marketing;</li>
<li>Facilitating academic, technical and professional advancement by encouraging the global development and exchange of research, knowledge, standards and procedures;</li>
<li>
Supporting communities by facilitating access to global knowledge and expertise and attracting new investment potential;
</li>
<li>
Enhancing and supporting transportation, hospitality and tourism infrastructure by creating an economically important rationale for non-leisure travel;
</li>
<li>
Promoting international cooperation and collaboration by encouraging and sustaining business and professional networks, and
</li>
<li>
Supporting economic transition by facilitating retraining and professional development on a global basis.
</li>
</ul>
<p>
The Meetings Industry has long recognised the importance of sustainability and environmental concerns, and as a result has developed extensive procedures and guidelines to actively address these issues as they relate to its own activities. However, it also acknowledges and promotes the essential importance of
face to face interactions in developing and maintaining personal and organisational relations as well as the efficiencies achieved by addressing business and professional outcomes through group activities such as conventions and exhibitions rather than the individual travel that would otherwise be required.
</p>
